package com.orange.students.calarasu.marius.week.two;

/*
 * VariabilePrimitive
	* creati 8 variabile : byte, short, int, long, float, double, boolean, char si sa le valorizati
		* type variable = value;
	* sa faceti print la cele 8 variabile.
	EX:  int valInt = 5;
		 System.out.println("valInt = " + valInt );
 */
public class VariabilePrimitive {

    public static void main(String[] args) {

        byte valByte = 126;
        short valShort = 100;
        int valInt = 520;
        long valLong = 100_000_000_000l;
        float valFloat = 1.2f;
        double valDouble = 2.562d;
        boolean valBoolean = true;
        char valChar = 'c';

        System.out.println("valByte = " + valByte );
        System.out.println("valShort = " + valShort);
        System.out.println("valInt = " + valInt);
        System.out.println("valLong = " + valLong);
        System.out.println("valFloat = " + valFloat);
        System.out.println("valDouble = " + valDouble);
        System.out.println("valBoleean = " + valBoolean);
        System.out.println("valChar = " + valChar);

    }

}
