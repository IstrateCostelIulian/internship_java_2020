package com.orange.topics.scannerPractice.ex1;

public class MathUtils {


    public static double impartire(double el1, double el2) {
        return el1 / el2;
    }

    public static double adunare(double el1, double el2) {
        return el1 + el2;
    }

    public static double scadere(double el1, double el2) {
        return el1 - el2;
    }

    public static double inmultire(double el1, double el2) {
        return el1 * el2;
    }

}
